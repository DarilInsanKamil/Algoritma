// Cetak bilangan bulat
// Cetak bilangan positif
// Cetak bilangan negatif
// Cetak bilangan genap dan ganjil
#include <iostream>
using namespace std;

int main()
{
    // Bilangan Bulat Positif dan Negatif
     int b = -5;
     cout << "Bilangan Bulat positif" << endl;
     for (int i = 1; i <= 5; i++)
     {
         cout << i << endl;
     }
     cout << "Bilangan Bulat negatif" << endl;
     for (int a = -1; a >= b; a--)
     {
         cout << a << endl;
     }

}